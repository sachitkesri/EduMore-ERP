package com.edumore.counsellor.bean;

public class EducationalQualification {
	
	private int educationId;
	private String qualification;
	private String status;
	private String college;
	private String unversity;
	private String completedYear;
	private String grade;
	private long enquiryNumber;
	
	public int getEducationId() {
		return educationId;
	}
	public void setEducationId(int educationId) {
		this.educationId = educationId;
	}
	public String getQualification() {
		return qualification;
	}
	public void setQualification(String qualification) {
		this.qualification = qualification;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCollege() {
		return college;
	}
	public void setCollege(String college) {
		this.college = college;
	}
	public String getUnversity() {
		return unversity;
	}
	public void setUnversity(String unversity) {
		this.unversity = unversity;
	}
	public String getCompletedYear() {
		return completedYear;
	}
	public void setCompletedYear(String completedYear) {
		this.completedYear = completedYear;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public long getEnquiryNumber() {
		return enquiryNumber;
	}
	public void setEnquiryNumber(long enquiryNumber) {
		this.enquiryNumber = enquiryNumber;
	}
	
	

}
